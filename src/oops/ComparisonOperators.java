package oops;

public class ComparisonOperators {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		
		//Equal To
		System.out.println(a == b);
		
		//Not Equal To
		System.out.println(a != b);
		
		//Greater Than
		System.out.println(a > b);
		
		//Less Than
		System.out.println(a < b);
		
		//Greater Than Equal To
		System.out.println(a >= b);
		
		//Less Than Equal To
		System.out.println(a <= b);

	}

}

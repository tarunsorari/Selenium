package oops;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		int a = 10;
		a += 2; // a = a + 2
		System.out.println(a);
		
		a -= 2; // a = a-2
		System.out.println(a);
		
		a *=2; // a = a*2
		System.out.println(a);
		
		a /= 2; // a = a/2
		System.out.println(a);
		
		a %= 2; // a = a/2 remainder value
		System.out.println(a);
		

	}

}
